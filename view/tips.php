                    <p>You reach for vitamin C when you feel a cold coming on, but did you know it also plays a role in delivering a smooth, glowing complexion? It’s become a holy-grail item for the skin-obsessed in recent years.

                        “Vitamin C is a hot topic, and it’s buzzworthy because it has so many preventative and also maintenance benefits,” says Nazanin Saedi, MD, a dermatologist with Jefferson University in Philadelphia.

                        Here, dive into everything you need to know about vitamin C serums, including why to use one, how to apply it, and what to watch out for.
                    <h2>What is a Vitamin C Serum Exactly, and What’s in It?</h2>
                    <p>A vitamin C serum is a skin-care product packed with vitamin C. It has the consistency of a liquid or gel, and is applied topically to the skin. You’ll find these serums wherever you normally purchase skin-care products — at department stores, beauty stores, and some drug stores, too. On the packaging, you’ll likely see words touting skin benefits like “firming,” “brightening,” and “collagen boosting.” Tapping into those benefits is the main reason to add a vitamin C serum to your skin-care routine.

                        “The benefit of vitamin C is it’s an antioxidant, so it helps prevent the forming of free radicals, which go on to break down collagen and elastin,” Saedi says. The result, she says, is glowing, youthful skin.

                        RELATED: 6 Ways to Eat and Drink Your Way to Healthier Skin</p>



                    <h2>Research Behind Vitamin C and Vitamin C Serums</h2>
                    <p>Of course, you can source vitamin C through foods, namely fruits and vegetables like cantaloupe, oranges, pineapple, broccoli, and Brussels sprouts, 
                        according to MedlinePlus. It’s an important nutrient to load up on because it’s essential for growth and development. It plays a role in forming
                        and repairing body tissue, and delivering antioxidants to cut down on the presence of dangerous free radicals.</p>

                    <P>Vitamin C is also naturally present in the skin, where it helps promote collagen and antioxidant levels, and protects the skin
                        against UV-ray-induced photodamage, according to a study published in August 2017 in the journal Nutrients. Sounds great, right? 
                        You may be thinking about how to increase vitamin C in your diet to tap into these benefits. Unfortunately, simply eating more 
                        oranges and cantaloupe isn’t going to cut it. That’s because the epidermis, the outermost layer of the skin, doesn’t have blood
                        vessels to send nutrients to the skin cells, according to the Nutrients study.</p>

                    <p>Vitamin C can, however, be applied topically — and that’s where vitamin C serums come in. “Vitamin C is best known for its ability 
                        to neutralize free radicals and improve aging skin,” says Joel Schlessinger, MD, an Omaha, Nebraska-based dermatologic surgeon and
                        RealSelf contributor. “It can also improve collagen and elastin synthesis — which can improve the appearance of fine lines and wrinkles;
                        help reduce inflammation, provide natural, mild exfoliation; and brighten skin.”</p>

                    <p>Research has also shown the effectiveness of topical application. A study published in September 2015 in Clinical, Cosmetic and 
                        Investigational Dermatology found applying vitamin C topically increased collagen production among study participants in all age groups. 
                        The study participants noticed the effects after 40 days of treatment and were even more pronounced at the 60-day mark.</p>

                    <p>It can also help with melasma and post-inflammatory hyperpigmentation. According to a January 2013 study published in Journal of Drugs and Dermatology,
                        study participants saw an average 73 percent improvement in skin pigmentation as a result of applying vitamin C to the face.</p


                    <h2>How to Use a Vitamin C Serum and Reap Its Benefits</h2>
                    <p>Saedi says to apply a few drops of serum to your face after you wash and dry it. “A little bit goes a really long way,”
                        she says. She recommends putting it on every morning, all year round. “It has some benefits with sun exposure so it helps 
                        to apply in the morning as the first layer of protection,” she says. She says to then layer on your moisturizer and sunscreen.
                        Serums are better than adding drops of vitamin C “boosters” to your moisturizer, Saedi says. They may not be as potent as serums 
                        themselves and mixing them with moisturizer may dilute the effects.</p>

                    <p>Store your bottle in a cool, dark place away from extreme heat, such as a bathroom cabinet as opposed to on your counter. “Storing 
                        a vitamin C serum correctly is integral to its success,” Schlessinger says. “A good rule of thumb is to toss the serum six months after
                        it’s been opened and to keep the serum in a cool, dark place.” If you’re using it according to the instructions, it’ll likely be gone before 
                        the six months is up. If not, you’ll want to throw it out. “When vitamin C is used past its shelf life, it can actually have negative effects,” 
                        Schlessinger says. More on that next..</p>

